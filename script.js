// learn how to use Type conversions

/*

    JavaScript Data Types

    In JavaScript there are 5 different data types that can contain values;

    -   string
    -   number
    -   boolean
    -   object
    -   function

    There are 6 types of objects;
    
    -   Object
    -   Date
    -   Array
    -   String
    -   Number
    -   Boolean

    And 2 data types that cannot contain values;

    -   null
    -   underfined

*/

var a = String(123);
console.log(a);

var b = Number(324);
console.log(b);

var c = String(Date());
console.log(c);

var d = Number(true);
console.log(d);

var e = Number(false);
console.log(e);